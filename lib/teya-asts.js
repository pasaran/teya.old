var asts = {};



//  ## Items
//  ---------------------------------------------------------------------------------------------------------------  //

asts.items = {};

asts.items._init = function() {
    this.items = [];
};

asts.items.add = function( item ) {
    this.items.push( item );
};

asts.items.toString = function() {
    if ( this.items.length > 0 ) {
        var r = this.items
            .join( '\n' )
            .replace( /^/gm, '    ' );

        return this.id + '\n' + r;
    }

    return this.id;
};



